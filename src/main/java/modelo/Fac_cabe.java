package modelo;

public class Fac_cabe {

    private String fac_num;
    private String fac_fec;
    private String clo_cod;
    private String fac_igv;
    private int ven_cod;

    public String getFac_num() {
        return fac_num;
    }

    public void setFac_num(String fac_num) {
        this.fac_num = fac_num;
    }

    public String getFac_fec() {
        return fac_fec;
    }

    public void setFac_fec(String fac_fec) {
        this.fac_fec = fac_fec;
    }

    public String getClo_cod() {
        return clo_cod;
    }

    public void setClo_cod(String clo_cod) {
        this.clo_cod = clo_cod;
    }

    public String getFac_igv() {
        return fac_igv;
    }

    public void setFac_igv(String fac_igv) {
        this.fac_igv = fac_igv;
    }

    public int getVen_cod() {
        return ven_cod;
    }

    public void setVen_cod(int ven_cod) {
        this.ven_cod = ven_cod;
    }
}
